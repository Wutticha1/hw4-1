// ‡πÄ‡∏Å‡πá‡∏ö‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£‡∏Ñ‡πà‡∏≤‡πÉ‡∏ä‡πâ‡∏à‡πà‡∏≤‡∏¢‡∏à‡∏≤‡∏Å LocalStorage ‡∏ñ‡πâ‡∏≤‡∏°‡∏µ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏≠‡∏¢‡∏π‡πà‡πÅ‡∏•‡πâ‡∏ß
const expenses = JSON.parse(localStorage.getItem("expenses")) || [];

// ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏•‡∏á LocalStorage
function saveExpensesToLocalStorage() {
    localStorage.setItem("expenses", JSON.stringify(expenses));
}

// ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏¢‡∏≠‡∏î‡∏£‡∏ß‡∏°‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î
function calculateTotal() {
    const sun_money = expenses.reduce((sum, expense) => sum + expense.amount, 0);
    document.getElementById("sum").textContent = `${sun_money} ‡∏ö‡∏≤‡∏ó`;
}

// ‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏Ñ‡πà‡∏≤‡πÉ‡∏ä‡πâ‡∏à‡πà‡∏≤‡∏¢
function addExpense(expenseData) {
    expenses.push(expenseData);
    saveExpensesToLocalStorage(); // ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏•‡∏á LocalStorage
    calculateTotal();
}

// ‡∏Ñ‡πâ‡∏ô‡∏´‡∏≤‡∏Ñ‡πà‡∏≤‡πÉ‡∏ä‡πâ‡∏à‡πà‡∏≤‡∏¢‡∏ï‡∏≤‡∏°‡∏ß‡∏±‡∏ô‡∏ó‡∏µ‡πà
function getExpenseByDate(date) {
    return expenses.filter(expense => expense.date === date);
}

// ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏¢‡∏≠‡∏î‡∏£‡∏ß‡∏°‡∏ï‡∏≤‡∏°‡∏õ‡∏£‡∏∞‡πÄ‡∏†‡∏ó
function calculateTotalByCategory(category) {
    return expenses
    .filter(expense => expense.category === category)
    .reduce((total, expense) => total + expense.amount, 0);
}

// ‡∏£‡∏≤‡∏¢‡∏á‡∏≤‡∏ô‡∏õ‡∏£‡∏∞‡∏à‡∏≥‡πÄ‡∏î‡∏∑‡∏≠‡∏ô
function MonthReport(month) {
    const monthExpense = expenses.filter(expense => new Date(expense.date).getMonth() + 1 === month);
    return monthExpense;
}


function filterExpenses() {
    const categoryMapping = {
        "Food": "‡∏Ñ‡πà‡∏≤‡∏≠‡∏≤‡∏´‡∏≤‡∏£ üç≤",
        "Transport": "‡∏Ñ‡πà‡∏≤‡πÄ‡∏î‡∏¥‡∏ô‡∏ó‡∏≤‡∏á ‚úàÔ∏è",
        "Entertainment": "‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ö‡∏±‡∏ô‡πÄ‡∏ó‡∏¥‡∏á üòú",
        "other": "‡∏≠‡∏∑‡πà‡∏ô‡πÜ"
    };
    
    const selectedCategory = categoryMapping[document.getElementById("filter-category").value] || "";
    const selectedDate = document.getElementById("filter-date").value;

    document.getElementById("expense-list").innerHTML = ""; // ‡∏•‡πâ‡∏≤‡∏á‡∏ï‡∏≤‡∏£‡∏≤‡∏á‡∏Å‡πà‡∏≠‡∏ô‡πÅ‡∏™‡∏î‡∏á‡∏ú‡∏•‡πÉ‡∏´‡∏°‡πà

    const filteredExpenses = expenses.filter(expense => {
        const matchCategory = !selectedCategory || expense.category === selectedCategory;
        const matchDate = !selectedDate || expense.date === selectedDate;

        return matchCategory && matchDate;
    });

    if (filteredExpenses.length === 0) {
        document.getElementById("expense-list").innerHTML =
            `<tr><td colspan="5" class="text-center text-gray-500 p-2">‡πÑ‡∏°‡πà‡∏°‡∏µ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ó‡∏µ‡πà‡∏ï‡∏£‡∏á‡∏Å‡∏±‡∏ö‡∏ï‡∏±‡∏ß‡∏Å‡∏£‡∏≠‡∏á</td></tr>`;
    } else {
        filteredExpenses.forEach(expense => addExpenseToTable(expense));
    }
}


